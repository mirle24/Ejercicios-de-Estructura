# Dados dos números calcule la suma, resta, multiplicación, división y módulo

print ("Ingresa un numero")
primerNumero = int(input())
print ("Ingresa otro numero")
segundoNumero = int(input())
print ("La suma es:",(primerNumero + segundoNumero), " \n " )
print("La resta es:", (primerNumero - segundoNumero)," \n " )
print("La multiplicación es:", (primerNumero * segundoNumero)," \n " )
print("La división es:",(primerNumero / segundoNumero)," \n " )
print("El módulo es:",(primerNumero % segundoNumero)," \n " )